  font_list.glyphs['W'] = GL_CAPITAL_W;
  font_list.glyphs['c'] = GL_SMALL_C;
  font_list.glyphs['e'] = GL_SMALL_E;
  font_list.glyphs['i'] = GL_SMALL_I;
  font_list.glyphs['l'] = GL_SMALL_L;
